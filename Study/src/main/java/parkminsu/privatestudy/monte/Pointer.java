package parkminsu.privatestudy.monte;

public class Pointer {
    double x;
    double y;

    public Pointer(double x, double y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public String toString() {
        return "Pointer{" +
                "x=" + x +
                ", y=" + y +
                '}';
    }
}
